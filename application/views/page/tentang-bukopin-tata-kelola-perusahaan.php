<p>b. Kriteria dan independensi anggota Dewan Komisaris dan Direksi</p>
<ul>
<ul>
<li>Dewan Komisaris<br>
Setiap anggota Dewan Komisaris dipilih dan diangkat dalam RUPS untuk masa jabatan empat tahun, dan masing-masing dapat dipilih kembali. Sesuai ketentuan Badan Pengawas Pasar Modal (Bapepam) dan peraturan Bursa Efek Indonesia (BEI), 3 (tiga) orang anggota Dewan Komisaris adalah Komisaris Independen, yang merupakan separuh atau lebih dari ketentuan 30% jumlah anggota Dewan Komisaris. Jumlah tersebut juga telah memenuhi ketentuan Bank Indonesia, yaitu minimal 50% dari jumlah anggota Dewan Komisaris.</li>
</ul>
</ul>
<p></p>
<ul>
<ul>
<li>Direksi<br>
Setiap anggota Direksi dipilih dan diangkat dalam RUPS untuk masa jabatan empat tahun, dan masing-masing dapat dipilih kembali.</li>
</ul>
</ul>
<p>c. Tugas dan tanggung jawab Dewan Komisaris dan Direksi</p>
<ul>
<ul>
<li>Komisaris<br>
Dewan Komisaris mempunyai tugas untuk melakukan pengawasan atas kebijakan yang diambil oleh Direksi dalam mengelola Bank; memberikan advis kepada Direksi sehubungan dengan tugas dan tanggung jawabnya, yang mencakup pelaksanaan review atas temuan Audit Internal dan memberikan rekomendasi tindak lanjut yang perlu dilakukan; memberikan masukan terkait persetujuan atas rencana strategi dan anggaran Bank, memonitor pelaksanaaan rencana dan pencapaian target usaha; serta memastikan kepatuhan Direksi terhadap prinsip-prinsip tata kelola perusahaan serta peraturan yang berlaku.</li>
</ul>
</ul>
<p></p>
<ul>
<ul>
<li>Direksi<br>
Direksi bertanggungjawab menyusun arah dan strategi perusahaan dengan persetujuan dari Dewan Komisaris dan selanjutnya bertugas untuk menjalankan kepengurusan perusahaan dan pengelolaan usaha secara efisien, untuk kepentingan perusahaan dan sesuai dengan maksud serta tujuan perusahaan.</li>
</ul>
</ul>
<p>d. Rekomendasi Dewan Komisaris</p>
<p style="padding-left: 30px;">Selama periode laporan (tahun 2007) Dewan Komisaris memberikan rekomendasi kepada Direksi antara lain</p>
<ul>
<ul>
<li>Menyetujui penerbitan pedoman prinsip mengenal nasabah Bank Bukopin.</li>
<li>Memberikan persetujuan atas kredit-kredit diatas kewenangan direksi.</li>
<li>Untuk memperhatikan ukuran-ukuran tingkat kesehatan bank seperti LDR, NPL.</li>
<li>Untuk memperhatikan peningkatan pelayanan yang lebih baik disegala bidang dalam menghadapi kompetisi&nbsp;&nbsp;&nbsp;&nbsp; persaingan perbankan yang sangat ketat.</li>
<li>Untuk menyesuaikan anggaran dasar Bank Bukopin dengan diberlakukannya UU No.40 tahun 2007 tentang Perseroan Terbatas.</li>
</ul>
</ul>
